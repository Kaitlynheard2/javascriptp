var fifteen= 15;

function addition(fifteen){
  if (fifteen == 15){
    console.log("2015 was a great year");
  }
  else if (fifteen < 15){
    console.log("before 2015 were the best years");
  }
  else if (fifteen > 15){
    console.log("after 2015 life was complicated");
  }
  else{
    console.log("oh well");
  }
}

addition(fifteen);
